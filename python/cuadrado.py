# Asigna a la variable lado el valor 3. Se medirá en metros.
lado = 3

# Calcula el perímetro y muéstralo indicando las unidades.
perímetro = lado * 4
# print("El perímetro del cuadrado es " + str(perímetro) + " metros.")
print('El perímetro del cuadrado es {0} metros.'.format(perímetro))

# Calcula el área y muéstrala indicando las unidades.
área = lado ** 2
# print("El área del cuadrado es " + str(área) + " metros cuadrados.")
print('El área del cuadrado es {0} metros cuadrados.'.format(área))
