# Pide base y altura
base = float(input('Introduzca la base: '))
altura = float(input('Introduzca la altura: '))

print ('El perímetro del rectángulo {0:.2f}x{1:.2f} es {2:.2f}m'.format(base,altura,(base*altura)*2))
print ('El área del rectángulo {0:.2f}x{1:.2f} es {2:.2f}m2'.format(base,altura,base*altura))

